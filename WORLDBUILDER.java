    public void init(){
        int y = 0;
        for(int[] row : map){            
            int x = 0;
            for(int value: row){
                BLOCK e = num_to_block(value);
                e.set_position(new POSITION(x*16,y*16));
                e.set_mode(BLOCKMODE.BLOCK);
                main_world.add_entity(e);
                x++;
            }
            y++;
        }
    }
    
    public BLOCK num_to_block(int number){
        switch(number){
            case 0: return new GRASS();
            case 1: return new WATER();
            case 2: return new BUSHONE();
            case 3: return new BUSHTWO();
            case 4: return new WOOD();
            case 5: return new STONE();
            case 6: return new WILDGARLIC();
            case 7: return new BROCCOLI();
            case 8: return new CARROT();
        }
        return null;
    }
